function mouseOverFeedr() {
    var div = document.getElementById("feedr-overlay");
    div.style.visibility = "visible";
}

function mouseOutFeedr() {
    var div = document.getElementById("feedr-overlay");
    div.style.visibility = "hidden";
}

function toggleFeedrVisibility() {
    var div = document.getElementById("feedr-overlay");
    div.style.visibility = ((div.style.visibility == "visible") ? "hidden" : "visible");
}

